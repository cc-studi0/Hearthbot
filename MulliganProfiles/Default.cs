        #region Mulligan Logic

        public List<Card.Cards> HandleMulligan(List<Card.Cards> choices, Card.CClass opponentClass, Card.CClass ownClass)
        {
            try
            {
                return HandleMulliganHSReplay(choices, opponentClass, ownClass);
            }
            catch (Exception ex)
            {
                Bot.Log("HSReplay mulligan failed: " + ex.Message + " - Using mana curve fallback.");
                CardsToKeep.Clear();
                _log = "";
                return HandleMulliganFallback(choices);
            }
        }

        private List<Card.Cards> HandleMulliganHSReplay(List<Card.Cards> choices, Card.CClass opponentClass, Card.CClass ownClass)
        {
            AddLog("\n----HSReplay Offline Mulligan----\n");

            // Select Standard or Wild data based on game mode
            bool isStandard = Bot.CurrentMode() == Bot.Mode.Standard ||
                              Bot.CurrentMode() == Bot.Mode.Casual ||
                              Bot.CurrentMode() == Bot.Mode.Practice;

            var decksByClass = isStandard ? StandardDecksByClass : WildDecksByClass;
            var mulliganByDeck = isStandard ? StandardMulliganByDeck : WildMulliganByDeck;
            string modeName = isStandard ? "RANKED_STANDARD" : "RANKED_WILD";

            string classKey = ownClass.ToString().ToUpper();
            AddLog("Mode : " + Bot.CurrentMode().ToString() + " (" + modeName + ")");
            AddLog("Selected Deck : " + Bot.CurrentDeck().Name);

            // Find matching deck entries for our class
            if (!decksByClass.ContainsKey(classKey) || decksByClass[classKey].Count == 0)
                throw new Exception("No deck data available for class: " + classKey);

            var entries = decksByClass[classKey];

            // Find closest matching deck
            string bestDeckId = "";
            int bestMatchPoints = 0;
            int bestMatchGames = 0;

            foreach (var entry in entries)
            {
                int points = GetMatchPoint(entry.DeckList, Bot.CurrentDeck());
                if (points > bestMatchPoints || (points == bestMatchPoints && entry.TotalGames > bestMatchGames))
                {
                    bestMatchPoints = points;
                    bestMatchGames = entry.TotalGames;
                    bestDeckId = entry.DeckId;
                }
            }

            if (string.IsNullOrEmpty(bestDeckId))
                throw new Exception("Could not find a matching deck.");

            AddLog("Matched Deck : https://hsreplay.net/decks/" + bestDeckId + "/#gameType=" + modeName);

            // Get mulligan data for the matched deck
            if (!mulliganByDeck.ContainsKey(bestDeckId))
                throw new Exception("No mulligan data for deck: " + bestDeckId);

            var mulliganCards = mulliganByDeck[bestDeckId];
            AddLog("MulliganEntries : " + mulliganCards.Count);

            // Convert to card IDs with keep percentages
            var sanitizedDatas = new List<MulliganResult>();
            foreach (var mc in mulliganCards)
            {
                var cardId = CardTemplate.TemplateList.FirstOrDefault(x => x.Value.DbfId == mc.DbfId).Key;
                sanitizedDatas.Add(new MulliganResult
                {
                    CardId = cardId,
                    KeptPercentage = mc.KeepPercentage,
                    OpeningHandWinrate = mc.OpeningHandWinrate,
                    WinrateWhenDrawn = mc.WinrateWhenDrawn,
                    WinrateWhenPlayed = mc.WinrateWhenPlayed
                });
            }

            var sortedDatas = sanitizedDatas.OrderByDescending(d => d.KeptPercentage).ToList();

            // Log full mulligan data
            AddLog("\nFull Mulligan Data:");
            foreach (var d in sortedDatas)
            {
                AddLog(CardTemplate.LoadFromId(d.CardId).Name + " - Kept : " + d.KeptPercentage.ToString("0.00") + "%");
            }

            // Log cards available in choices
            AddLog("\nCards to choose from:");
            foreach (var d in sortedDatas.Where(d => choices.Contains(d.CardId)))
            {
                AddLog(CardTemplate.LoadFromId(d.CardId).Name + " - Kept : " + d.KeptPercentage.ToString("0.00") + "%");
            }

            // Keep cards with kept percentage >= 75
            AddLog("\nCards to Keep:");
            bool hasPlayableOnOne = false;

            foreach (var d in sortedDatas.Where(d => choices.Contains(d.CardId) && d.KeptPercentage >= 75))
            {
                if (!CardsToKeep.Contains(d.CardId))
                {
                    AddLog(CardTemplate.LoadFromId(d.CardId).Name + " - Kept : " + d.KeptPercentage.ToString("0.00") + "%");
                    Keep(d.CardId);
                }

                var cost = CardTemplate.LoadFromId(d.CardId).Cost;
                if (cost == 1 || (cost == 2 && choices.Contains(Card.Cards.GAME_005)))
                    hasPlayableOnOne = true;
            }

            // If we have a playable turn 1 card with >= 75% keep, also keep cards with 50-75%
            if (hasPlayableOnOne)
            {
                foreach (var d in sortedDatas.Where(d =>
                             choices.Contains(d.CardId) &&
                             d.KeptPercentage >= 50 &&
                             d.KeptPercentage < 75 &&
                             !CardsToKeep.Contains(d.CardId)))
                {
                    AddLog(CardTemplate.LoadFromId(d.CardId).Name + " - Kept : " + d.KeptPercentage.ToString("0.00") + "%");
                    Keep(d.CardId);
                }
            }

            AddLog("");
            AddLog("----------HSReplay Offline Mulligan----------");
            AddLog("");
            Bot.Log(_log);
            return CardsToKeep;
        }

